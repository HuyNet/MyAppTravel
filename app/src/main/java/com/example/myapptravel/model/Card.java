package com.example.myapptravel.model;

public class Card {
    int idDL;
    String tenDL;
    String batdau;
    String ketthuc;
    long giave;
    String hinnhanh;
    int soluong;
    public Card(){
    }

    public int getIdDL() {
        return idDL;
    }

    public void setIdDL(int idDL) {
        this.idDL = idDL;
    }

    public String getTenDL() {
        return tenDL;
    }

    public void setTenDL(String tenDL) {
        this.tenDL = tenDL;
    }

    public String getBatdau() {
        return batdau;
    }

    public void setBatdau(String batdau) {
        this.batdau = batdau;
    }

    public String getKetthuc() {
        return ketthuc;
    }

    public void setKetthuc(String ketthuc) {
        this.ketthuc = ketthuc;
    }

    public long getGiave() {
        return giave;
    }

    public void setGiave(long giave) {
        this.giave = giave;
    }

    public String getHinnhanh() {
        return hinnhanh;
    }

    public void setHinnhanh(String hinnhanh) {
        this.hinnhanh = hinnhanh;
    }

    public int getSoluong() {
        return soluong;
    }

    public void setSoluong(int soluong) {
        this.soluong = soluong;
    }
}
